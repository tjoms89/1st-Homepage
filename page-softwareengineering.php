                    <h2><a name="t4" id="t4">XSL Transformation</a></h2>
                    <p>
                        XSL steht für Extensible Stylesheet Language und hängt zusammen mit den XML-basierten Transformationssprachen XSLT und XSL-FO.  Diese Transformationssprachen bedienen sich  XPath, um Inhalte der XML-Dateien zu referenzieren. Sie dienen der Umwandlung von XML (Extensible Markup Language) Dokumenten.
                        XML Dokumente basieren auf einer hierarchischen Baumstruktur in Form von Textdateien.
                        Mit XSLT (XSL Transformation) lassen sich Stylesheets (Formatvorlagen) zur Umgestaltung und Formatierung des XML Dokuments und seinen Elementen entwerfen. XPath liefert die passende Sprache zur Navigation durch das XML Dokument. Mit ihr lassen sich gezielt Elemente auslesen, die mittels XSLT weiterverarbeitet werden können. Dabei liest ein XSLT Prozessor (diese sind in den meisten modernen Webbrowsern integriert) die XML Quelldatei und das Stylesheet ein und erstellt daraufhin ein neues Dokument in der gewünschten Struktur.
                        XSL-FO (XSL– Formatting Objects) ist eine auf XML basierende Anwendung, die beschreibt wie grafische Elemente auf einer Seite angeordnet werden sollen. Mit einem FO-Prozessor können anhand eines XSL-FO Stylesheets aus XML Dokumenten auch PDF, RTF und ASCII Texte generiert werden.
                        Unsere Anwendungsgebiete umfassen die komplette Integration und Entwicklung, d.h. die Erstellung von XML Dateien aus ERP Systemen (z.B. SAP) bis hin zum pdf-Outputdokument mit folgenden Sprachen: 
                        -	XSLT 1.0 und 2.0
                        -	XPath
                        -	XSL-FO
                        -	XQuery
                        -	XSL Schemaentwicklung

